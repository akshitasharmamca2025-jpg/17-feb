# Read principal, rate, and time from input
P = float(input("Enter principal amount: "))
R = float(input("Enter rate of interest: "))
T = float(input("Enter time in years: "))

# Calculate simple interest
SI = (P * R * T) / 100

# Print the result
print("Simple Interest:", SI)
